//--------------------------------------------------------------------------------------------------
// Imports Section:
//--------------------------------------------------------------------------------------------------
import { Pipe }           from '@angular/core';
import { PipeTransform }  from '@angular/core';

//--------------------------------------------------------------------------------------------------
// Pipe Component Declaration Section:
//--------------------------------------------------------------------------------------------------
@Pipe({
        name: 'numbersToPixels'
})
//--------------------------------------------------------------------------------------------------
// Pipe Class Declaration Section:
//--------------------------------------------------------------------------------------------------
export class NumbersToPixelsPipe implements PipeTransform 
{
    //----------------------------------------------------------------------------------------------
    // Main Transform Method Section:
    //----------------------------------------------------------------------------------------------
    transform(value: any, args?: any): any 
    {
      return value.toString() + 'px';
    }
}
